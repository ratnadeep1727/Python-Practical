a = 10
b = 20

# Simple if statement
if a < b: 
    print("a is less than b")

# Simple if-else statement
if a < b: 
    print("a is less than b")
else:
    print("a is greater than b")

# Nested if statement
if a < b:
    print("a is less than b")
    if b - a > 5:  # Nested condition
        print("The difference between b and a is greater than 5")
    else:
        print("The difference between b and a is 5 or less")
